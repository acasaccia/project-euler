"use strict";

var BigInt = require("../BigInt.js");

describe("BigInt", function(){

    it("should sum big integers", function(){
        var a = new BigInt("162348172356451826548726541287458245187451987459185619874569183475619834718147856");
        var b = new BigInt("12654458127564812746927569478256197815696254236854182745128451872564236451278451278412784518");
        var c = a.add(b);
        expect(c.value).toEqual("12654458127727160919284021304804924356983712482041634732587637492438805634754071113130932374");
    })

});